#include "main.h"

/* Addition */
int add(int a, int b)
{
	return (a + b);
}

/* Substraction */
int sub(int a, int b)
{
	return (a - b);
}

/* Multiplication */
int mul(int a, int b)
{
	return (a * b);
}

/* Division */
int div(int a, int b)
{
	return (a / b);
}

/* Modulo */
int mod(int a, int b)
{
	return (a % b);
}
